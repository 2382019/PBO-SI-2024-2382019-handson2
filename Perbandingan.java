public class Perbandingan {
    public static void main(String[] args) {
        int nilai = 70; // operand 1 (nilai)
        int minimumKelulusan = 60; // operand 2 (minimumKelulusan)
        // == sama dengan
        System.out.println(nilai == minimumKelulusan);
        // != tidak sama dengan
        System.out.println(nilai != minimumKelulusan);
        // >
        System.out.println(nilai > minimumKelulusan);
        // <
        System.out.println(nilai < minimumKelulusan);
        // >=
        System.out.println(nilai >= minimumKelulusan);
        // <=
        System.out.println(nilai <= minimumKelulusan);
    }
}
